def add(a, b):
    """This function adds two numbers,where a,b are two inputs given by user"""
    return a + b

def subtract(a, b):
    """This function subtracts two numbers,where a,b are two inputs given by user"""
    return a - b

def multiply(a, b):
    """This function multiplies two numbers.where a,b are two inputs given by user"""
    return a * b
